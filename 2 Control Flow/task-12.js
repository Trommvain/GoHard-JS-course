// Найдите сумму положительных элементов массива.

// Использовать встроенные методы массивов — нельзя.

// ```javascript
// const array = [2, -1, -3, 15, 0, 4];
// ```

const array = [2, -1, -3, 15, 0, 4];
let sum = 0;

for (let i of array) {
    if (i >= 0) sum += i;
}

console.log(sum);